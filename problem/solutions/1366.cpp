#include<bits/stdc++.h>
using namespace std;
int n,k;
int w[100005],a[100005],vv[100005];
vector<int> b[100005];
void dfs(int x,int y,int z){
	if(vv[x]==1) return;
	vv[x]=1;
	a[x]=z;
	for(int i=0;i<b[x].size();i++)if(b[x][i]!=y)dfs(b[x][i],x,z+1);
	return;
}
int main(){
    cin>>n>>k;
    for(int i=1;i<=n;i++)cin>>w[i];
    for(int i=1;i<n;i++){
    	int u,v;
    	cin>>u>>v;
    	b[u].push_back(v);
    	b[v].push_back(u);
	}
	dfs(1,0,0);
	long long ans=0;
	for(int i=1;i<=n;i++)if(a[i]<=2*k)ans+=w[i];
	cout<<ans;
	return 0;
}
